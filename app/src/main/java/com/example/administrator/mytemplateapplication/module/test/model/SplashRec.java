public class SplashRec {
    private String id;
    private String title;
    private String content;
    private String activity;
    private String ios_activity;
    private String android_activity;
    private String start_time;
    private String end_time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }

    public String getIos_activity() {
        return ios_activity;
    }

    public void setIos_activity(String ios_activity) {
        this.ios_activity = ios_activity;
    }

    public String getAndroid_activity() {
        return android_activity;
    }

    public void setAndroid_activity(String android_activity) {
        this.android_activity = android_activity;
    }

    public String getStart_time() {
        return start_time;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public String getEnd_time() {
        return end_time;
    }

    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }
}
